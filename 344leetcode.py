'''
Write a function that reverses a string. The input
string is given as an array of characters s.
You must do this by modifying the input array
in-place with O(1) extra memory.
'''

from audioop import reverse


class Solution:
    def reverseString():
        s = ['h', 'e', 'l', 'l', 'o']
        s.reverse()
        print(s)
    
    reverseString()

